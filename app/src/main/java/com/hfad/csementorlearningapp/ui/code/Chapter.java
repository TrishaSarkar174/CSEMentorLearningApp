package com.hfad.csementorlearningapp.ui.code;

public class Chapter {
    private String name;
    private String imageUrl;

    public Chapter(String name, String imageUrl) {
        this.name = name;
        this.imageUrl = imageUrl;
    }

    public String getName() {
        return name;
    }

    public String getImageUrl() {
        return imageUrl;
    }
}
